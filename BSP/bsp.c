/*
*********************************************************************************************************
*	函 数 名: bsp_Init
*	功能说明: 初始化所有的硬件设备。该函数配置CPU寄存器和外设的寄存器并初始化一些全局变量。只需要调用一次
*	形    参: 无
*	返 回 值: 无
*********************************************************************************************************
*/
void bsp_Init(void)
{  
  //tim
  __HAL_TIM_CLEAR_FLAG(&htim5, TIM_SR_UIF);	//避免启动定时器后立即进入定时器中断
	HAL_TIM_Base_Start_IT(&htim5);
   	
	BSP_LED_Init();    	        /* 初始化LED */
	bsp_InitKey();              /* 初始化按键 */
	usart_configure();          /* 初始化串口 */

	BSP_INA303_Init();
	MOTOR_Init();
	
}

/**************************************************************
==> 功  能：空闲调用（主循环尽可能快执行）
***************************************************************/
void hal_fast_loop(void){
	usart_dataparse();       /* 串口FIFO轮询发送 */
	read_usart_state();      /* 检查串口发送缓存区 */
}

/**************************************************************
==> 功  能：1000Hz(1ms)调用 —— 已在TIM5中断中执行，保证时序精度
***************************************************************/
void hal_1000hz_loop(void){
	/* 先执行INA303采样与滤波，确保后续状态机使用最新数据 */
	BSP_INA303_FastTask();   /* INA303滤波、保护、ADC3轮询(Vbus/Temp) */
	MOTOR_SlowTask_1kHz();   /* 电机状态机 + 故障检测（依赖上面的最新数据） */
}

