/*
*********************************************************************************************************
*   模块名称 : INA303/BEMF/Vbus/Tempr 采集驱动配置（注入组版本）
*   文件名称 : bsp_ina303_cfg.h
*   版    本 : V2.1
*   说    明 : 适配实际原理图：
*              - ADC1 注入组：Iu(PC0/Ch10), Iv(PC1/Ch11), Iw(PC2/Ch12) — TIM1_CC4 RISING 触发
*              - ADC2 注入组：BEMF_A(PA0/Ch0), BEMF_B(PA1/Ch1), BEMF_C(PA2/Ch2) — TIM1_CC4 RISING 触发
*              - ADC3 规则组：Vbus(PC3/Ch13), Temp(PA3/Ch3) — 软件轮询 1kHz
*
*   ⚠️  关键警告：
*       1. 注入组采样时间必须在 CUBEMX 中设为 3 Cycles！
*          当前 ADC 时钟 = APB2(84MHz)/DIV4 = 21MHz。
*          若用 15 Cycles，3 通道转换时间 = 3*(15+12)/21M = 3.86us，
*          而 95% duty 下桥臂窗口仅 2.5us，转换会被打断！
*          改为 3 Cycles 后：3*(3+12)/21M = 2.14us < 2.5us，安全。
*
*       2. CCR4 必须已在 CUBEMX 中改为 4050，触发沿改为 RISING！
*********************************************************************************************************
*/

#ifndef __BSP_INA303_CFG_H__
#define __BSP_INA303_CFG_H__

#include "stdint.h"
#include "adc.h"
#include "tim.h"

/* =======================================================================================================
 * 第一节：ADC 硬件句柄（与 CUBEMX 生成名称严格一致）
 * ======================================================================================================= */
#define INA303_ADC1_HANDLE              hadc1
#define INA303_ADC2_HANDLE              hadc2
#define INA303_ADC3_HANDLE              hadc3

/* =======================================================================================================
 * 第二节：注入组通道映射（与 CUBEMX Injected Rank 严格对应）
 * ======================================================================================================= */

/* ADC1 注入组：三相电流 */
#define INA303_ADC1_INJ_NUM             3
#define INA303_ADC1_INJ_RANK_IU         0   /* Rank1 -> JDR1: PC0 / ADC1_IN10 */
#define INA303_ADC1_INJ_RANK_IV         1   /* Rank2 -> JDR2: PC1 / ADC1_IN11 */
#define INA303_ADC1_INJ_RANK_IW         2   /* Rank3 -> JDR3: PC2 / ADC1_IN12 */

/* ADC2 注入组：三相 BEMF（反电动势） */
#define INA303_ADC2_INJ_NUM             3
#define INA303_ADC2_INJ_RANK_BEMF_A     0   /* Rank1 -> JDR1: PA0 / ADC2_IN0 */
#define INA303_ADC2_INJ_RANK_BEMF_B     1   /* Rank2 -> JDR2: PA1 / ADC2_IN1 */
#define INA303_ADC2_INJ_RANK_BEMF_C     2   /* Rank3 -> JDR3: PA2 / ADC2_IN2 */

/* =======================================================================================================
 * 第三节：规则组通道映射（ADC3 软件轮询）
 * ======================================================================================================= */
#define INA303_ADC3_REG_NUM             2
#define INA303_ADC3_REG_RANK_VBUS       0   /* Rank1: PC3 / ADC3_IN13 */
#define INA303_ADC3_REG_RANK_TEMP       1   /* Rank2: PA3 / ADC3_IN3 */

/* =======================================================================================================
 * 第四节：TIM1 CH4 触发点
 * ======================================================================================================= */
/* CCR4 = 4050，确保在下桥臂导通窗口内触发（最大占空比 95%，对应 CCR ≈ 3990） */
#define INA303_TIM1_CCR4_VALUE          4050

/* =======================================================================================================
 * 第五节：INA303 电流采样电路参数
 * ======================================================================================================= */
#define INA303_RSENSE_OHM               0.0002f     /* 0.2 mΩ */
#define INA303_GAIN_VV                  20.0f       /* INA303A1 固定增益 20 V/V */
#define INA303_VREF_V                   3.3f
#define INA303_ADC_RESOLUTION           4096.0f     /* 12位 */

/* 双向电流检测偏置（INA303 REF 引脚实际接 1.6V） */
#define INA303_ZERO_OFFSET_V            1.6f
#define INA303_DEFAULT_OFFSET_ADC       (int16_t)((INA303_ZERO_OFFSET_V / INA303_VREF_V) * INA303_ADC_RESOLUTION)

/* =======================================================================================================
 * 第六节：母线电压分压（Vbus）
 * ======================================================================================================= */
#define INA303_VBUS_DIV_R1_OHM          18000.0f    /* 上电阻，接母线正 */
#define INA303_VBUS_DIV_R2_OHM          1000.0f     /* 下电阻，接 GND */
#define INA303_VBUS_DIV_RATIO           ((INA303_VBUS_DIV_R2_OHM) / ((INA303_VBUS_DIV_R1_OHM) + (INA303_VBUS_DIV_R2_OHM)))

/* =======================================================================================================
 * 第七节：BEMF 分压
 * =======================================================================================================
 * 实际分压：R1=51kΩ, R2=1kΩ，分压比=1/52，最大量程≈171.6V
 * 95Vac 峰压=134V，分压后≈2.58V < 3.3V，安全 */
#define INA303_BEMF_DIV_R1_OHM          51000.0f    /* 上电阻 */
#define INA303_BEMF_DIV_R2_OHM          1000.0f     /* 下电阻 */
#define INA303_BEMF_DIV_RATIO           ((INA303_BEMF_DIV_R2_OHM) / ((INA303_BEMF_DIV_R1_OHM) + (INA303_BEMF_DIV_R2_OHM)))

/* =======================================================================================================
 * 第八节：温度传感器（⚠️ 请根据实际传感器型号修改！）
 * =======================================================================================================
 * 说明：当前假设为简单的模拟温度传感器或 NTC 分压输出。
 *       TEMP_SCALE 的单位是 °C/LSB。若使用 NTC，建议改为查表法。 */
#define INA303_TEMP_OFFSET_ADC          0           /* 0°C 对应的 ADC 值（占位） */
#define INA303_TEMP_SCALE_C_PER_LSB     0.1f        /* 占位，请按实际传感器校准 */

/* =======================================================================================================
 * 第九节：量程与保护阈值
 * ======================================================================================================= */
#define INA303_CURRENT_MAX_A            70.0f
#define INA303_BEMF_MAX_V               60.0f       /* BEMF 有效值上限 */
#define INA303_VBUS_MAX_V               60.0f
#define INA303_OC_THRESHOLD_A           65.0f
#define INA303_UC_THRESHOLD_A           (-65.0f)
#define INA303_OV_THRESHOLD_V           50.0f
#define INA303_UV_THRESHOLD_V           15.0f

#define INA303_PROT_DEBOUNCE_CNT        5
#define INA303_PROT_HYSTERESIS          0.9f

/* =======================================================================================================
 * 第十节：ADC2 BEMF 使能开关（启动阶段可禁用以节省中断开销）
 * =======================================================================================================
 * 说明：启动阶段（0~4s）不需要 BEMF，设 0 禁用 ADC2 注入组。
 *       后续进入发电模式检测前，调用 BSP_INA303_ADC2_Start() 启用。 */
#define INA303_USE_ADC2_BEMF            0

/* =======================================================================================================
 * 第十一节：软件滤波
 * ======================================================================================================= */
#define INA303_FILTER_SHIFT             3
#define INA303_FILTER_SIZE              (1U << INA303_FILTER_SHIFT)

/* =======================================================================================================
 * 第十二节：默认增益校准
 * ======================================================================================================= */
#define INA303_DEFAULT_GAIN_CAL         1.0f

#endif /* __BSP_INA303_CFG_H__ */
/***************************** (END OF FILE) *********************************/
